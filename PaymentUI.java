    private void ProccessBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProccessBttnActionPerformed

    String cardText = CardNumberFeild.getText().trim();
        String cvvText  = CVVFeild.getText().trim();
        String expDate  = DateFeild.getText().trim();
        String custID   = CustomerIDFeild.getText().trim();

        // 1) Validate inputs
        if (cardText.isEmpty() || cvvText.isEmpty() || expDate.isEmpty() || custID.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "All fields must be filled in.",
                "Error", JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        if (!cardText.matches("\\d{8}")) {
            JOptionPane.showMessageDialog(this,
                "Card number must be exactly 8 digits.",
                "Error", JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        if (!cvvText.matches("\\d{3}")) {
            JOptionPane.showMessageDialog(this,
                "CVV must be exactly 3 digits.",
                "Error", JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        if (!expDate.matches("(0[1-9]|1[0-2])/\\d{2}")) {
            JOptionPane.showMessageDialog(this,
                "Expiration date must be in MM/YY format.",
                "Error", JOptionPane.ERROR_MESSAGE
            );
            return;
        }

       
        java.sql.Connection conn = null;
        Statement stmt = null;
        try {
            conn = DB.getInstance().getConnection();
            stmt = conn.createStatement();

            // check if the customer id exists in the db
            java.sql.ResultSet rs = stmt.executeQuery(
                "SELECT * FROM Customer WHERE customerID = '" + custID + "'"
            );
            if (!rs.first()) {
                JOptionPane.showMessageDialog(this,
                    "Customer ID `" + custID + "` not found.",
                    "Error", JOptionPane.ERROR_MESSAGE
                );
                return;
            }


            // 5) dummy amounts 
            int amount  = 500;
            int balance = 600;
            int cardNum = Integer.parseInt(cardText);
            int cvv     = Integer.parseInt(cvvText);

            //  validate via controller
            PaymentController controller = new PaymentController();
            controller.CheckPaymentDetails(cardNum, cvv, expDate);
            controller.CheckBalance(amount, balance);

            // make payment object and apply decorator
            Payment p1 = new Payment( amount, "card",cardNum, cvv, expDate, balance, Integer.parseInt(custID));
            PaymentComponent payment = new Payment( amount, "card",cardNum, cvv, expDate, balance, Integer.parseInt(custID));
            if (DiscountRadio.isSelected()) {
                payment = new DiscountDecorator(payment, 50);
            } else if (CashBackRadio.isSelected()) {
                payment = new CashBackDecorator(payment, 50);
            }

            // 8) show message according to the radio button choice (decorator)
            String message = payment.getPaymentMessage();
            int confirm = JOptionPane.showConfirmDialog(
                this, message + "\nProceed?",
                "Confirm Payment", JOptionPane.YES_NO_OPTION
            );
            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }

            // 9) ensure funds are sufficient post‚Äêdecoration
            if (payment.getBalance() < payment.getAmount()) {
                JOptionPane.showMessageDialog(this,
                    "Insufficient funds after discount/cashback.",
                    "Error", JOptionPane.ERROR_MESSAGE
                );
                return;
            }

            p1.saveToDatabase(payment);

            JOptionPane.showMessageDialog(this,
                "Payment saved successfully.",
                "Success", JOptionPane.INFORMATION_MESSAGE
            );

            
            CardNumberFeild.setText("");
            CVVFeild.setText("");
            DateFeild.setText("");
            CustomerIDFeild.setText("");
            DiscountRadio.setSelected(false);
            CashBackRadio.setSelected(false);
            
            OLDcustomer oc=new OLDcustomer();
            oc.setVisible(true);
            this.dispose();

        } catch (Exception e) {
            System.err.println("ERROR in processing payment: " + e);
            JOptionPane.showMessageDialog(this,
                "An unexpected error occurred:\n" + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE
            );
        } 
        
        
    }//GEN-LAST:event_ProccessBttnActionPerformed

